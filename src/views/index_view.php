                        <h2>
                            Historia komiksów - bardziej współczesna
                        </h2>
                        <p>Od połowy lat osiemdziesiątych do wczesnych dziewięćdziesiątych popularni byli antybohaterowie. Panowały mroczne, pesymistyczne historie,
                             jak w Watchmen Alana Moore'a, gdzie świat spogląda z góry na niegdyś potężnych superbohaterów
                              lub w Batman: The Dark Knight Returns Franka Millera, gdzie 55-letni Batman wycofał się z walki z przestępczością, pozostawiając przestępców do terroryzowania miasta Gotham.
                               Czytelnicy byli świadkami śmierci Supermana, ciężkiej kontuzji Batmana oraz Green Lanterna Hala Jordana mordującego innych Green Lanternów.</p>
                        <p>W okresie tym, nazywanym The Dark Age, ukazała się również nagrodzony Pulitzerem Maus, poruszająca,
                             autobiograficzna opowieść Arta Spiegelmana o rodzinie żydowskiej w Polsce żyjącej za czasów nazistowskich Niemiec, gdzie polacy są ukazani jako myszy, a okupanci jako koty.</p>
                        <p>Publikacja Kingdom Come Alexa Rossa w 1996 roku, nawiązująca do optymizmu i siły superbohaterów z lat 50. XX wieku , oznacza początek epoki współczesnej.</p>
                        <p>Po ponad osiemdziesięciu latach od debiutu Supermana, przemysł komiksowy utrzymał się dzięki wczesnemu przyjęciu komiksów cyfrowych,
                             pomyślnemu nasyceniu rynków filmowych i telewizyjnych oraz utrzymywaniu silnych więzi z ich rzeszą fanów.</p>
                        <p>Po ogromnym niepowodzeniu filmu Batman i Robin (1997), filmy o superbohaterach zostały umieszczone na lodzie w celu ich przerobienia. W 2000 roku skromny sukces filmu X-Men pomógł przywrócić popularność filmów o superbohaterach,
                             a filmowe uniwersum Marvela powstające od dekady weszło na dobre do głównego nurtu popkultury.</p>
