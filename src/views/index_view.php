                        <h2>
                            Historia komiksów
                        </h2>
                        <p>Prekursorami komiksów były karykatury i rysunki, które zdobyły popularność w Wielkiej Brytanii oraz Stanach Zjedoczonych w XIX wieku.
                             Były one często satyryczne oraz komentowały aktualną sytuację polityczną w tych krajach.</p>
                        <p>Komiksy z superbohaterami zaczęły się ukazywać dopiero w latach 30 XX wieku.
                            Jednym z pierwszych tego typu komiksów był <i>Action Comics</i>. Na łamach tego czasopisma zadebiutował wtedy Superman.</p>
                        <p>Okres od 1938 do połowy lat czterdziestych XX wieku to szczyt popularności komiksów.
                             Podczas gdy obecna miesięczna sprzedaż popularnych tytułów komiksów oscyluje wokół 100 000 egzemplarzy,
                              na początku lat 40. tytuły Supermana, Batmana i Kapitana Marvela były regularnie sprzedawane w ilości 1,5 miliona egzemplarzy miesięcznie.</p>
                        <p>Podczas powrotu do normalności w powojennej Ameryce sprzedaż komiksów o superbohaterach gwałtownie spadła, a wiele tytułów zaprzestano wydawać.
                             W połowie lat pięćdziesiątych pustkę wypełniły komiksy zawierające bardziej poważne tematy, takie jak przestępczość, romans, western i horror.</p>
                        <p>Od późnych lat pięćdziesiątych do sześćdziesiątych XX wieku nastąpiła zmiana z mrocznych i nadprzyrodzonych
                             motywów komiksowych na drugi koniec spektrum z książkami zawierającymi głupkowate fabuły i wysoki stopień estetyki kampu.</p>
                        <p>Społecznie świadome historie stały się liczniejsze w latach 70. XX wieku, najsłynniej podczas wspólnych przygód Green Lanterna i Green Arrow, którzy walczyli z rasizmem, zanieczyszczeniem i niesprawiedliwością społeczną.
                             Green Arrow zmierzył się także z uzależnieniem od heroiny swojego pomocnika Speedy'ego, podczas gdy Iron Man pogodził się ze swoim alkoholizmem.</p>
                        
